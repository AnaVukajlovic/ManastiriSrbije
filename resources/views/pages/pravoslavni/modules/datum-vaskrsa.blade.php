<section class="ps-asec">
  <h2>Zašto se datum Vaskrsa menja</h2>
  <p class="ps-ap">
    Vaskrs je pokretan praznik. Njegov datum se određuje prema drevnom crkvenom pravilu (paschaliji),
    koje povezuje prolećnu ravnodnevicu, pun mesec i nedelju.
  </p>
  <ul class="ps-ul">
    <li>Vaskrs se slavi u <strong>nedelju</strong>.</li>
    <li>Datum zavisi od crkvenog računanja proleća i mesečevih faza (paschalni pun mesec).</li>
  </ul>
</section>

<section class="ps-asec">
  <h2>Osnovno pravilo (pojednostavljeno)</h2>
  <p class="ps-ap">
    Vaskrs se slavi prve nedelje posle “paschalnog” punog meseca koji dolazi nakon prolećne ravnodnevice.
    Ovo je pojednostavljenje, ali pomaže da se razume logika.
  </p>
</section>

<section class="ps-asec">
  <h2>Zašto se razlikuje od nekih zapadnih datuma</h2>
  <p class="ps-ap">
    U praksi, razlika nastaje zbog različitih kalendarskih računanja i načina računanja paschalije.
    U aplikaciji ovo može biti objašnjeno mirno i informativno, bez rasprava.
  </p>
</section>

<section class="ps-asec">
  <h2>Šta još “ide” uz Vaskrs</h2>
  <ul class="ps-ul">
    <li><strong>Veliki post</strong> — priprema kroz podvig.</li>
    <li><strong>Cveti</strong>, <strong>Veliki petak</strong>, <strong>Velika subota</strong> — praznični tok.</li>
    <li><strong>Spasovdan</strong> i <strong>Duhovi</strong> — praznici posle Vaskrsa.</li>
  </ul>
</section>

