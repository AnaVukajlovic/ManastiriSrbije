<section class="ps-asec">
  <h2>Zanimljivosti iz pravoslavnog sveta</h2>
  <p class="ps-ap">
    U ovom modulu su kratka objašnjenja simbola i običaja.
    Cilj je da vernik razume <em>zašto</em> nešto postoji, a ne samo “kako se radi”.
  </p>
</section>

<section class="ps-asec">
  <h2>Ikone</h2>
  <p class="ps-ap">
    Ikona je “prozor ka nebu” — svedočanstvo da se Bog ovaplotio i da svet može biti osvećen.
    Ikone se poštuju (ne “obožavaju”) kao i Jevanđelje i Krst: poštovanje ide ka Onome koji je na ikoni prikazan.
  </p>
  <ul class="ps-ul">
    <li><strong>Ikonostas</strong> razdvaja oltar i naos, ali i povezuje: prikazuje istoriju spasenja.</li>
    <li><strong>Kandilo</strong> ispred ikone simbol je molitve i budnosti.</li>
  </ul>
</section>

<section class="ps-asec">
  <h2>Tamjan i kadionica</h2>
  <p class="ps-ap">
    Tamjan je simbol molitve koja se uzdiže Bogu, kao i poštovanja svetinje.
    Kadionica se koristi u bogosluženju da označi prisustvo blagodati i svečanost molitve.
  </p>
</section>

<section class="ps-asec">
  <h2>Znamenje krsta</h2>
  <p class="ps-ap">
    Krst je znak pripadnosti Hristu i ispovedanje vere. Pravoslavni se krste desnom rukom:
    tri spojena prsta (Sveta Trojica) i dva prsta prema dlanu (dve prirode Hristove).
  </p>
  <div class="ps-callout">
    <strong>Praktično:</strong> Krstimo se mirno, bez žurbe — nije “gest”, nego kratka molitva.
  </div>
</section>

<section class="ps-asec">
  <h2>Zašto palimo sveće</h2>
  <p class="ps-ap">
    Sveća je simbol svetlosti Hristove i naše molitve. Paljenje sveće je tihi prinos:
    “Gospode, primi moju molitvu”. Najvažnije je unutrašnje stanje, ne broj sveća.
  </p>
</section>

<section class="ps-asec">
  <h2>Slava</h2>
  <p class="ps-ap">
    Slava je srpski pravoslavni običaj — dan porodičnog svetitelja zaštitnika.
    U centru slave nisu “trpeza i gosti”, nego molitva, sveća, žito i kolač, i blagoslov doma.
    (Kasnije ovaj deo može biti poseban modul “Slave i običaji”.)
  </p>
</section>