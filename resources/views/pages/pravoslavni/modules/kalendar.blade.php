<section class="ps-asec">
  <h2>Šta je pravoslavlje</h2>
  <p class="ps-ap">
    Pravoslavlje je život u Crkvi — zajednici koja veru ne doživljava samo kao učenje, već kao iskustvo:
    molitvu, bogosluženje, post, ljubav prema bližnjem, pokajanje i radost Vaskrsenja.
    U središtu svega je Hristos, a cilj života je zajednica sa Bogom (spasenje) kroz blagodat.
  </p>
  <p class="ps-ap">
    Važno je razumeti: vera se ne svodi na “pravila”, već na odnos sa Bogom.
    Pravila (post, molitveno pravilo, priprema za pričešće) postoje da pomognu, ne da postanu teret.
  </p>
</section>

<section class="ps-asec">
  <h2>Crkva i Predanje</h2>
  <p class="ps-ap">
    Crkva čuva i prenosi veru kroz Sveto Pismo i Sveto Predanje.
    Predanje je živo iskustvo Crkve: bogoslužbeni život, dela Svetih Otaca, odluke Vaseljenskih sabora,
    ikonopis, crkveni kalendar i način života koji se oblikovao vekovima.
  </p>
  <ul class="ps-ul">
    <li><strong>Sveto Pismo</strong> — temeljno svedočanstvo vere.</li>
    <li><strong>Sveto Predanje</strong> — način na koji Crkva razume i živi Pismo.</li>
    <li><strong>Liturgija</strong> — “škola vere” kroz molitvu i bogosluženje.</li>
  </ul>
</section>

<section class="ps-asec">
  <h2>Liturgija</h2>
  <p class="ps-ap">
    Božanska Liturgija je centralno bogosluženje i srce crkvenog života.
    Na Liturgiji se okupljamo kao zajednica, slušamo Jevanđelje, molimo se i prinosimo darove,
    a vrhunac je Sveto Pričešće.
  </p>
  <ul class="ps-ul">
    <li><strong>Zašto nedelja?</strong> Nedelja je “mali Vaskrs” — dan Hristovog Vaskrsenja.</li>
    <li><strong>Zašto je važna priprema?</strong> Da bi se Liturgija živela s pažnjom i smirenjem.</li>
    <li><strong>Kako da pristupim?</strong> Jednostavno: dođi na vreme, budi sabran, slušaj i moli se.</li>
  </ul>
</section>

<section class="ps-asec">
  <h2>Molitva</h2>
  <p class="ps-ap">
    Molitva je razgovor srca sa Bogom. Može biti kratka i tiha, ili čitana iz molitvenika.
    Važnije od “mnogo reči” je pažnja i smirenje.
  </p>
  <ul class="ps-ul">
    <li><strong>Jutarnja i večernja</strong> — osnovno molitveno pravilo.</li>
    <li><strong>Isusova molitva</strong> — kratka molitva sa pažnjom (“Gospode Isuse Hriste, pomiluj me”).</li>
    <li><strong>Zajednička molitva</strong> — u hramu, na Liturgiji i bogosluženjima.</li>
  </ul>
  <div class="ps-callout">
    <strong>Savet:</strong> Ako tek počinješ, uzmi kratko pravilo (5–10 min) i budi istrajna.
  </div>
</section>

<section class="ps-asec">
  <h2>Post</h2>
  <p class="ps-ap">
    Post je podvig koji uključuje i telo i dušu: uzdržanje u hrani,
    ali još više uzdržanje od zla, sujete, osuđivanja i svađe.
    Post bez milosrđa i molitve postaje samo dijeta.
  </p>
  <ul class="ps-ul">
    <li><strong>Post na vodi / na ulju / riblji dan</strong> — različiti stepeni uzdržanja.</li>
    <li><strong>Smisao posta</strong> — čišćenje srca, jačanje volje, budnost duše.</li>
    <li><strong>U praksi</strong> — post se usklađuje uz blagoslov (zdravlje, posao, trudnoća…).</li>
  </ul>
</section>

<section class="ps-asec">
  <h2>Ispovest i Pričešće</h2>
  <p class="ps-ap">
    Ispovest je susret sa Bogom u istini: priznanje grehova i početak isceljenja.
    Pričešće je sjedinjenje sa Hristom — najveći dar i vrhunac liturgijskog života.
  </p>
  <ul class="ps-ul">
    <li><strong>Kako se pripremiti?</strong> Molitva, post (koliko je moguće), mir sa ljudima.</li>
    <li><strong>Zašto ispovest?</strong> Da se srce očisti i nauči pokajanju.</li>
    <li><strong>Najvažnije</strong> — pristupiti smireno, bez osuđivanja drugih.</li>
  </ul>
</section>

